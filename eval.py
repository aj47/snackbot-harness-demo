#!/usr/bin/env python3
"""Fixed evaluator for the SnackBot harness demo.

Do not edit this file during attempts.
"""

from __future__ import annotations

import argparse
import ast
import hashlib
import importlib.util
import json
import sys
import time
from pathlib import Path
from typing import Any

ROOT = Path(__file__).resolve().parent
CITY_PATH = ROOT / "data" / "city.json"
TRAIN_PATH = ROOT / "data" / "orders_train.json"
EVAL_PATH = ROOT / "data" / "orders_eval.json"
SOLUTION_PATH = ROOT / "strategy.py"

FORBIDDEN_IMPORTS = {
    "os",
    "sys",
    "subprocess",
    "multiprocessing",
    "socket",
    "requests",
    "urllib",
    "pathlib",
    "shutil",
    "glob",
    "pickle",
    "random",
}
FORBIDDEN_CALLS = {"open", "exec", "eval", "compile", "input", "__import__"}
FORBIDDEN_SUBSTRINGS = [
    "orders_eval",
    "data/",
    "baselines/",
    "logs/",
    "subprocess",
    "requests",
    "urllib",
    "socket",
]


def load_json(path: Path) -> Any:
    return json.loads(path.read_text())


def sha256(path: Path) -> str:
    return hashlib.sha256(path.read_bytes()).hexdigest()


def static_checks(source_path: Path, orders: list[dict[str, Any]]) -> list[str]:
    violations: list[str] = []
    source = source_path.read_text()

    for text in FORBIDDEN_SUBSTRINGS:
        if text in source:
            violations.append(f"forbidden substring: {text}")

    try:
        tree = ast.parse(source)
    except SyntaxError as exc:
        return [f"syntax error: {exc}"]

    for node in ast.walk(tree):
        if isinstance(node, ast.Import):
            for alias in node.names:
                root_name = alias.name.split(".")[0]
                if root_name in FORBIDDEN_IMPORTS:
                    violations.append(f"forbidden import: {alias.name}")
        elif isinstance(node, ast.ImportFrom):
            root_name = (node.module or "").split(".")[0]
            if root_name in FORBIDDEN_IMPORTS:
                violations.append(f"forbidden import: {node.module}")
        elif isinstance(node, ast.Call):
            func = node.func
            if isinstance(func, ast.Name) and func.id in FORBIDDEN_CALLS:
                violations.append(f"forbidden call: {func.id}")

    # A light hardcoding guard: individual IDs can appear in examples/comments, but
    # many eval IDs usually means a lookup table or memorized route.
    eval_ids = [order["id"] for order in orders]
    mentioned = [order_id for order_id in eval_ids if order_id in source]
    if len(mentioned) >= 4:
        violations.append(f"too many eval order IDs mentioned: {', '.join(mentioned[:8])}")

    return sorted(set(violations))


def import_strategy(path: Path):
    module_name = f"snackbot_strategy_{time.time_ns()}"
    spec = importlib.util.spec_from_file_location(module_name, path)
    if spec is None or spec.loader is None:
        raise RuntimeError("could not import strategy.py")
    module = importlib.util.module_from_spec(spec)
    spec.loader.exec_module(module)
    if not hasattr(module, "plan_route"):
        raise RuntimeError("strategy.py must define plan_route(city, orders)")
    return module


def manhattan(a: list[int] | tuple[int, int], b: list[int] | tuple[int, int]) -> int:
    return abs(int(a[0]) - int(b[0])) + abs(int(a[1]) - int(b[1]))


def crosses_blocked(a: tuple[int, int], b: tuple[int, int], blocked: set[tuple[int, int]]) -> int:
    """Approximate blocked-cell crossings along a deterministic Manhattan path.

    This keeps the demo simple: agents do not need full pathfinding, but routes that
    repeatedly cut through blocked cells get penalized.
    """
    x, y = a
    bx, by = b
    hits = 0
    step_x = 1 if bx >= x else -1
    while x != bx:
        x += step_x
        if (x, y) in blocked:
            hits += 1
    step_y = 1 if by >= y else -1
    while y != by:
        y += step_y
        if (x, y) in blocked:
            hits += 1
    return hits


def score_route(city: dict[str, Any], orders: list[dict[str, Any]], route: Any) -> dict[str, Any]:
    order_by_id = {order["id"]: order for order in orders}
    blocked = {tuple(cell) for cell in city.get("blocked", [])}
    max_steps = int(city["max_steps"])
    travel_cost = int(city.get("travel_cost", 1))
    late_penalty = int(city.get("late_penalty", 6))
    deadline_bonus = int(city.get("deadline_bonus", 3))

    violations: list[str] = []
    if not isinstance(route, list):
        return {
            "score": -1000,
            "delivered": 0,
            "travel_steps": 0,
            "late_orders": 0,
            "deadline_bonus": 0,
            "invalid_route_penalty": 1000,
            "violations": ["route must be a list"],
        }

    position = tuple(city["start"])
    seen: set[str] = set()
    travel_steps = 0
    delivered = 0
    total_tip = 0
    late_orders = 0
    bonus_points = 0
    invalid_penalty = 0
    blocked_hits = 0

    for item in route:
        if not isinstance(item, str):
            invalid_penalty += 25
            violations.append("route contains non-string item")
            continue
        if item not in order_by_id:
            invalid_penalty += 25
            violations.append(f"unknown order id: {item}")
            continue
        if item in seen:
            invalid_penalty += 15
            violations.append(f"duplicate order id: {item}")
            continue

        order = order_by_id[item]
        dest = tuple(order["location"])
        leg = manhattan(position, dest)
        travel_steps += leg
        blocked_hits += crosses_blocked(position, dest, blocked)
        position = dest
        seen.add(item)

        if travel_steps <= max_steps:
            delivered += 1
            total_tip += int(order["tip"])
            if travel_steps <= int(order["deadline"]):
                bonus_points += deadline_bonus
            else:
                late_orders += 1
        else:
            # Visiting after max_steps wastes travel and earns no tip.
            invalid_penalty += 8

    invalid_penalty += blocked_hits * 4
    overtime = max(0, travel_steps - max_steps)
    invalid_penalty += overtime * 2

    score = total_tip + bonus_points - (travel_steps * travel_cost) - (late_orders * late_penalty) - invalid_penalty

    return {
        "score": int(score),
        "delivered": delivered,
        "total_orders": len(orders),
        "travel_steps": travel_steps,
        "max_steps": max_steps,
        "total_tip": total_tip,
        "late_orders": late_orders,
        "deadline_bonus": bonus_points,
        "blocked_hits": blocked_hits,
        "invalid_route_penalty": invalid_penalty,
        "route_length": len(route),
        "violations": sorted(set(violations)),
    }


def evaluate(split: str) -> dict[str, Any]:
    city = load_json(CITY_PATH)
    orders = load_json(TRAIN_PATH if split == "train" else EVAL_PATH)

    violations = static_checks(SOLUTION_PATH, orders)
    start = time.perf_counter()
    route: Any = []
    runtime_error = None
    try:
        module = import_strategy(SOLUTION_PATH)
        route = module.plan_route(json.loads(json.dumps(city)), json.loads(json.dumps(orders)))
    except Exception as exc:  # noqa: BLE001 - evaluator should report failures cleanly
        runtime_error = repr(exc)
    runtime_ms = (time.perf_counter() - start) * 1000

    if runtime_ms > 500:
        violations.append(f"runtime too slow: {runtime_ms:.1f}ms > 500ms")
    if runtime_error:
        violations.append(f"runtime error: {runtime_error}")

    metrics = score_route(city, orders, route)
    violations.extend(metrics.pop("violations", []))
    constraints_passed = not violations
    if not constraints_passed:
        metrics["score"] = min(int(metrics.get("score", 0)), -1000)

    return {
        "score": metrics["score"],
        **metrics,
        "constraints_passed": constraints_passed,
        "violations": sorted(set(violations)),
        "runtime_ms": round(runtime_ms, 3),
        "split": split,
        "strategy_sha256": sha256(SOLUTION_PATH),
    }


def main() -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument("--split", choices=["train", "eval"], default="eval")
    parser.add_argument("--json", action="store_true", help="print compact JSON only")
    args = parser.parse_args()

    result = evaluate(args.split)
    if args.json:
        print(json.dumps(result, sort_keys=True))
    else:
        print(json.dumps(result, indent=2, sort_keys=True))
    return 0 if result["constraints_passed"] else 1


if __name__ == "__main__":
    raise SystemExit(main())
